public class Radio {
    public void turnOn()
    {
        System.out.println("radio is turned on");
    }


    public void turnOff()
    {
        System.out.println("radio is turned off");
    }


    public void volumeUp()
    {
        System.out.println("radio volume is louder");
    }

    public void volumeDown()
    {
        System.out.println("radio volume is quieter");
    }
}
